// Función para abrir un modal dado su ID
function openModal(id) {
    document.getElementById(id).style.display = 'block'; // Muestra el modal modificando su estilo
}

// Función para cerrar un modal dado su ID
function closeModal(id) {
    document.getElementById(id).style.display = 'none'; // Oculta el modal modificando su estilo
}
